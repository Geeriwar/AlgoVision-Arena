// prim.cpp
#include "prim.h"
#include <queue>
#include <vector>
#include <functional>

using namespace std;

//////////////////////////////////////////////////////////////
// Prim's Algorithm
// n   = number of nodes
// adj = adjacency list {neighbor, weight}
//
// returns steps:
// each step = {nodeIncluded, edgeWeightUsed}
//
// Example:
// {0,0}
// {2,1}
// {1,2}
// {3,1}
//////////////////////////////////////////////////////////////

vector<vector<int>> prim(
    int n,
    vector<vector<pair<int,int>>>& adj
) {
    //////////////////////////////////////////////////
    // key = minimum edge weight to connect node
    //////////////////////////////////////////////////
    vector<int> key(n, 1000000000);

    //////////////////////////////////////////////////
    // Track included nodes
    //////////////////////////////////////////////////
    vector<int> inMST(n, 0);

    //////////////////////////////////////////////////
    // Min heap => {weight, node}
    //////////////////////////////////////////////////
    priority_queue<
        pair<int,int>,
        vector<pair<int,int>>,
        greater<pair<int,int>>
    > pq;

    //////////////////////////////////////////////////
    // Start from node 0
    //////////////////////////////////////////////////
    key[0] = 0;
    pq.push({0, 0});

    //////////////////////////////////////////////////
    // Visualization steps
    //////////////////////////////////////////////////
    vector<vector<int>> steps;

    //////////////////////////////////////////////////
    // Main loop
    //////////////////////////////////////////////////
    while (!pq.empty()) {

        pair<int,int> top = pq.top();
        pq.pop();

        int w = top.first;
        int u = top.second;

        //////////////////////////////////////////////////
        // Skip if already used
        //////////////////////////////////////////////////
        if (inMST[u]) continue;

        //////////////////////////////////////////////////
        // Include node in MST
        //////////////////////////////////////////////////
        inMST[u] = 1;

        //////////////////////////////////////////////////
        // Save step
        // {node, edgeWeight}
        //////////////////////////////////////////////////
        steps.push_back(
            vector<int>{u, w}
        );

        //////////////////////////////////////////////////
        // Relax neighbors
        //////////////////////////////////////////////////
        for (auto &edge : adj[u]) {

            int v  = edge.first;
            int wt = edge.second;

            if (
                !inMST[v] &&
                wt < key[v]
            ) {
                key[v] = wt;
                pq.push({wt, v});
            }
        }
    }

    //////////////////////////////////////////////////
    // Safety fallback
    //////////////////////////////////////////////////
    if (steps.empty()) {
        steps.push_back(
            vector<int>{0,0}
        );
    }

    return steps;
}