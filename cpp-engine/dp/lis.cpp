#include "lis.h"
#include <vector>
#include <algorithm>

using namespace std;

//////////////////////////////////////////////////////////////
// Longest Increasing Subsequence
// arr = input array
//
// returns steps:
// each step = current DP array snapshot
//////////////////////////////////////////////////////////////

vector<vector<int>> lis(
    vector<int>& arr
) {
    int n = arr.size();

    //////////////////////////////////////////////////
    // Safety for empty input
    //////////////////////////////////////////////////
    if (n == 0) {
        return {
            vector<int>{0}
        };
    }

    //////////////////////////////////////////////////
    // dp[i] = LIS ending at i
    //////////////////////////////////////////////////
    vector<int> dp(n, 1);

    //////////////////////////////////////////////////
    // Visualization steps
    //////////////////////////////////////////////////
    vector<vector<int>> steps;

    //////////////////////////////////////////////////
    // Build DP
    //////////////////////////////////////////////////
    for (int i = 0; i < n; i++) {

        for (int j = 0; j < i; j++) {

            if (
                arr[i] > arr[j]
            ) {
                dp[i] = max(
                    dp[i],
                    dp[j] + 1
                );
            }
        }

        //////////////////////////////////////////////////
        // Save current DP state
        //////////////////////////////////////////////////
        steps.push_back(dp);
    }

    //////////////////////////////////////////////////
    // Safety fallback
    //////////////////////////////////////////////////
    if (steps.empty()) {
        steps.push_back(dp);
    }

    return steps;
}